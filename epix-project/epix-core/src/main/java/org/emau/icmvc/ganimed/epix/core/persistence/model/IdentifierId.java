import java.io.Serializable;

public class IdentifierId implements Serializable {

	private static final long serialVersionUID = 1581784997738744750L;

	private Long domain;
	private String value;
	
	public IdentifierId() {
	}
	
	public IdentifierId(Long domain, String value) {
		this.domain = domain;
		this.value = value;
	}
	
	public Long getDomain() {
		return domain;
	}
	
	public String getValue() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((domain == null) ? 0 : domain.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (!(obj instanceof IdentifierId))
			return false;
		IdentifierId other = (IdentifierId) obj;
		if (domain == null) {
			if (other.domain != null)
				return false;
		} else if (!domain.equals(other.domain))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}
