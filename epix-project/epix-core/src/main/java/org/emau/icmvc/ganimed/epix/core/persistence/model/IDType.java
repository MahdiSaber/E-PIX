import java.io.Serializable;
import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * 
 * @author Christian Schack
 *
 */

@Entity
@Table(name="id_type")
public class IDType implements Serializable {
	private static final long serialVersionUID = 1125799122732378001L;

	private static final String UNDEFINED_VALUE = "Undefined";

	public static final IDType UNDEFINED = new IDType(UNDEFINED_VALUE);

	@Id @GeneratedValue(strategy=GenerationType.IDENTITY)
	private Long id;
	
	@Column(name="value")
	private String value;
	
	@Column(name="description")
	private String desc;
	
	@Column(name="entry_date")
	private Timestamp entryDate; 
	
	public IDType() {
	}
	
	public IDType(String value) {
		this.value = value;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	public Timestamp getEntryDate() {
		return entryDate;
	}

	public void setEntryDate(Timestamp entryDate) {
		this.entryDate = entryDate;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		IDType other = (IDType) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "IDType [id=" + id + ", value=" + value + ", desc=" + desc
				+ ", entryDate=" + entryDate + "]";
	}
}
