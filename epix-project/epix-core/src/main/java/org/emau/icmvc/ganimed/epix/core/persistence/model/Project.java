import java.io.Serializable;
import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 * 
 * @author Christian Schack
 * @since 08.11.2010
 */

@Entity
@Table(name = "project")
public class Project implements Serializable {

	private static final long serialVersionUID = 6318099646964125994L;

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long id;

	@Column(name = "name")
	private String name;

	@Column(name = "description")
	private String desc;

	@ManyToOne(optional = false)
	private IDType idType;

	@Column(name = "entry_date")
	private Timestamp entryDate;

	@ManyToOne(optional = false)
	@JoinColumn(name = "mpi_domain")
	private IdentifierDomain mpiDomain;

	public Project() {
	}

	public Project(String name) {
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public IDType getIdType() {
		return idType;
	}

	public void setIdType(IDType idType) {
		this.idType = idType;
	}

	public Timestamp getEntryDate() {
		return entryDate;
	}

	public void setEntryDate(Timestamp entryDate) {
		this.entryDate = entryDate;
	}

	public IdentifierDomain getMpiDomain() {
		return mpiDomain;
	}

	public void setMpiDomain(IdentifierDomain mpiDomain) {
		this.mpiDomain = mpiDomain;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Project other = (Project) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Project [" + (id != null ? "id=" + id + ", " : "") + (name != null ? "name=" + name + ", " : "")
				+ (desc != null ? "desc=" + desc + ", " : "") + (idType != null ? "idType=" + idType + ", " : "")
				+ (entryDate != null ? "entryDate=" + entryDate + ", " : "")
				+ (mpiDomain != null ? "mpiDomain=" + mpiDomain : "") + "]";
	}

}
